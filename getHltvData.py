def get_data(url):
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64; rv:88.0) Gecko/20100101 Firefox/88.0'}

    return BeautifulSoup(requests.get(url, headers=headers).content, 'html.parser')


def getPlayer(url, startDate='2021-04-12', endDate='2021-05-08'):

    soup = get_data(
        f'{url}?startDate={startDate}&endDate={endDate}')
    jogador = soup.find('h1').text

    status = [i.text for i in soup.find_all(
        'div', {'class': 'summaryStatBreakdownDataValue'})]

    objeto = {}

    for i in soup.find_all(
            'div', {'class': 'stats-row'}):
        spans = i.find_all('span')
        objeto[spans[0].text] = spans[1].text

    def formatarTexto(i):
        spans = i.find_all('span')
        return {'pais': i.find('img')[
            'title'], 'nome': spans[0].text, 'rating': spans[1].text}

    teams = [formatarTexto(team) for team in soup.find_all(
        'div', {'class': 'teammate-info'})]
    time.sleep(3)
    return {'jogador': jogador, 'status': status, 'objeto': objeto, 'teams': teams}


